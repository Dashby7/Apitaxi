											<br/>
											<br/>

											<h3>Place du Capitole</h3>
											<span class="image featured"><img src="images/capitole.png" alt="Place du Capitole" /></span>
											<p>La place du Capitole est le point central de la ville de Toulouse, son sol est orné de la croix Occitane représentant les 12 points cardinaux et elle est bordée de magnifiques bâtiments contenant de nombreux commerces ainsi que les fameuses arcades. Elle est aussi le point de rassemblement de tous les citoyens de Toulouse lors de grandes occasions. Il est donc impossible de ne pas y passer lors d’une visite de Toulouse.</p>
											<br/>

											<h3>Basilique Saint-Sernin</h3>
											<span class="image featured"><img src="images/ssernin.png" alt="St Sernin" /></span>
											<p>La basilique Saint Sernin est le bâtiment Chrétien le plus important de Toulouse. Elle fût bâtie durant le 11ème siècle afin d’abriter les reliques de Saint Saturnin, le premier évêque de Toulouse. Elle est aujourd’hui classée au titre de monument historique et inscrite au patrimoine de l’UNESCO. Sa nef longue de 115 mètres, ses orgues, son clocher et ses nombreuses peintures font d’elle l’un des piliers touristiques de la ville.</p>
											<br/>

											<h3>Cité de l'espace</h3>
											<span class="image featured"><img src="images/cespace.png" alt="" /></span>
											<p>Toulouse étant la capitale Européenne de l’aérospatial, il serait dommage de ne pas faire un détour pas la cité de l’espace. Avec ses nombreuses expositions interactives vous permettant de simuler une promenade sur la lune ou d'embarquer sur la station Mir, la cité de l’espace vous permet aussi d’en apprendre beaucoup sur la Terre et l’Univers grâce à son immense télescope, son planétarium ou encore son cinéma IMAX. De quoi passer un bon moment tout en apprenant, seul ou en famille.</p>
											<br/>

											<h3>Musée des Augustins</h3>
											<span class="image featured"><img src="images/augustin.png" alt="" /></span>
											<p>Pour tous les adeptes de musées, ce n’est pas ce qu’il manque à Toulouse. Du Muséum d’Histoire Naturelle jusqu’aux Abattoirs, il y’en a pour tous les goûts. Le musée des Augustins est le musée des beaux-arts de Toulouse, il abrite de nombreuses peintures et sculptures historiques et il est donc un passage obligatoire pour tous les amateurs d’art.</p>
											<br/>

											<h3>Lourdes</h3>
											<span class="image featured"><img src="images/lourdes.png" alt="" /></span>
											<p>La commune de Lourdes est le centre de pèlerinage catholiques en France et le deuxième lieu de pèlerinage le plus important d’Europe derrière le Vatican. Elle est surtout réputée pour sa fameuse grotte qui permettrait de guérir toutes les maladies.</p>
											<br/>

											<h3>Cité de Carcassonne</h3>
											<span class="image featured"><img src="images/carcassonne.png" alt="" /></span>
											<p>Cette cité médiévale fortifiée datant de l’époque gallo-romaine est l’une des plus réputée puisqu’elle fût sauvée de la destruction et parfaitement entretenue au fil des années. Elle est composée d’un château et d’une basilique ainsi que du village bordé de remparts. Elle est classée au patrimoine de l’UNESCO depuis 1997.</p>
											<br/>

											<h3>Cordes Sur Ciel</h3>
											<span class="image featured"><img src="images/cordessurciel.png" alt="" /></span>
											<p>Cordes Sur Ciel est une commune située dans le département du Tarn, elle est un des points touristiques les plus importants de l’Occitanie puisqu’elle l’une des étapes du pèlerinage de Saint-Jacques-De-Compostelle. Ses rues tortueuses et ses maisons rappelant le Moyen Age ainsi que sa vue dominante des alentours en valent le détour.</p>
											<br/>

											<h3>Gouffre de Padirac</h3>
											<span class="image featured"><img src="images/padirac.png" alt="" /></span>
											<p>Le gouffre de Padirac est le premier site souterrain du monde. Il est situé dans le Lot proche de la ville de Rocamadour, connue pour son fromage. Profond de 103 mètres, il y est proposé une visite guidée où l’on peut suivre une rivière souterraine en marchant mais aussi en canoë. On y découvre les galeries de roches sculptées sur les parois créant un paysage aussi magnifique qu’impressionnant. </p>
